def set_nested_value(data, key_path, value):
    """Set a nested value in dict using dot notation like 'home.hero.slide1.title'"""
    keys = key_path.split(".")
    current = data
    for key in keys[:-1]:
        if key not in current:
            current[key] = {}
        current = current[key]
    current[keys[-1]] = value


